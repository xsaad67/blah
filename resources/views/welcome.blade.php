                    <article class="article-post">
                        <p>Some of the most serious injuries among older adults, age 65 and older, are caused by falling. More than 1.6 million older Americans end up in the emergency room or hospital because of a fall, according to the National Institutes
                            of Health. Seniors who have broken a hip by falling can have trouble recovering and regaining mobility.<br /> The good news is many falls are preventable. One of the first things you can do if you take prescription medication
                            is have your health care team review your medication.</p>
                        <blockquote>
                            <p>“Some prescription medicines and over-the-counter drugs, or a combination of them, can make you dizzy or sleepy. Either can lead to a fall,” said Jaza Marina, M.D., a geriatrician at Kaiser Permanente in Atlanta. “If you fall,
                                be sure to let your doctor know, even if you aren’t hurt. Sometimes falls are a sign of a new medical problem that needs attention.”</p>
                        </blockquote>
                        <p>Many underlying causes of falls can be treated or corrected. Dr. Marina <strong>recommends these 10 proactive steps</strong> to reduce the risk of falling. Make your home safe.</p>
                        <p>1. Remove clutter, throw rugs and electrical cords that might cause you to trip.<br /> 2. Store items on bottom shelves.<br /> 3. Add grab bars where necessary — in hallways, stairways and bathtubs.<br /> 4. Add a rubber bath mat
                            in the shower or tub.<br /> 5. Make sure your home is well lit. Use night lights in hallways and bathrooms.<br /> 6. Keep a phone and flashlight by your bed.<br /> Take care of yourself.<br /> 7. Stay as physically active as
                            you can.<br /> 8. Wear comfortable shoes with good support.<br /> 9. Have your vision and hearing checked.<br /> 10. Use a cane or walker if you feel unsteady.</p>
