                    <img width="1400" height="700" src="../wp-content/uploads/sites/29/2017/11/fig-08-11-2017_02-10-58.jpg" class="featured-image img-fluid wp-post-image" alt="" srcset="http://www.themepush.com/demo-mediumish/wp-content/uploads/sites/29/2017/11/fig-08-11-2017_02-10-58.jpg 1400w, http://www.themepush.com/demo-mediumish/wp-content/uploads/sites/29/2017/11/fig-08-11-2017_02-10-58-300x150.jpg 300w, http://www.themepush.com/demo-mediumish/wp-content/uploads/sites/29/2017/11/fig-08-11-2017_02-10-58-768x384.jpg 768w, http://www.themepush.com/demo-mediumish/wp-content/uploads/sites/29/2017/11/fig-08-11-2017_02-10-58-1024x512.jpg 1024w"
